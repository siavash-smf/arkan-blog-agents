import "server-only";
import { runAgentJSON } from "@/lib/ai";
import { COMPANY_PROFILE, BRAND_VOICE } from "@/lib/company";
import { lessonsBlockFor } from "./lessons";
import { SocialBriefSchema, type SocialBrief, type SocialIdea } from "./types";

/**
 * ایجنت — زاویه‌یاب لینکدین
 *
 * وظیفه: یک «مشاهده‌ی خام» را به بریف اجتماعی تبدیل کند.
 *
 * چرا این ایجنت با استراتژیست اینستاگرام فرق دارد؟
 * ماده‌ی خامشان فرق دارد. کاروسل اینستاگرام از ایده‌ای می‌آید که ایده‌یاب
 * تولید کرده؛ ولی پست لینکدینِ خوب معمولاً از چیزی می‌آید که مشاور در
 * هفته‌ی گذشته *واقعاً دیده* — یک الگوی تکراری در جلسه‌ها، یک اشتباه که
 * سه کلاینت پشت سر هم کردند. این جنس محتوا اعتبار می‌سازد، چون قابل جعل
 * نیست.
 *
 * پس ورودی اصلی این پایپ‌لاین «مشاهده» است، نه «موضوع». اگر مشاهده‌ای
 * ندادید، از ایده‌های ایده‌یاب استفاده می‌شود — ولی خروجی معمولاً عمومی‌تر
 * و کم‌اثرتر خواهد بود، و این را صادقانه در پرامپت هم می‌گوییم.
 */
export async function runLinkedinAngleFinder(input: {
  /** مشاهده‌ی خام — منبع ترجیحی */
  observation: string | null;
  /**
   * آیا این مشاهده را انسان نوشته یا خودِ سیستم تولیدش کرده؟
   *
   * ⚠️ این پرچم از یک باگ واقعی درآمد. در اولین اجرای کمپین،
   * استراتژیستِ کمپین در زاویه‌ی لینکدین آمار ساخت («۱۰٪ رشد فروش، ۲٪
   * کاهش سود») و ارکستریتور همان را در جایگاه «مشاهده» گذاشت. زاویه‌یاب
   * هم درست عمل کرد: قاعده‌اش می‌گوید چیزی که در ورودی نیست نساز — و آن
   * اعداد *در ورودی بودند*. نتیجه: یک پست کاملاً باورپذیر با آمار جعلی.
   *
   * درس: «مشاهده‌ی انسان» و «زاویه‌ی تولیدشده‌ی مدل» دو چیزند. اولی واقعیت
   * است، دومی فقط یک جهت. اگر این تمایز را در تایپ نگه نداریم، توهمِ یک
   * ایجنت به‌عنوان واقعیت وارد ایجنت بعدی می‌شود.
   */
  observationIsTrusted: boolean;
  /** جایگزین وقتی مشاهده‌ای نیست */
  ideas: SocialIdea[];
}): Promise<SocialBrief> {
  const lessons = await lessonsBlockFor("linkedin-angle-finder");

  const system = `تو «زاویه‌یاب لینکدین» آرکان هستی. کارت این است که از یک مشاهده‌ی خام، آن زاویه‌ای را پیدا کنی که در فید لینکدین باعث توقف و گفت‌وگو می‌شود.

${COMPANY_PROFILE}

${BRAND_VOICE}

قواعد:
- پست لینکدینِ خوب از **تجربه** می‌آید، نه از تعریف. اگر مشاهده‌ای در اختیارت هست، هسته‌ی محتوا باید همان باشد؛ آن را به یک درس عمومی تعمیم بده، ولی جزئیات ملموسش را دور نریز — همان جزئیات است که پست را باورپذیر می‌کند.
- **هیچ عدد، آمار یا جزئیاتی که در ورودی نیست از خودت نساز.** اگر مشاهده مبهم بود، به‌جای پرکردن جای خالی با حدس، همان را کلی‌تر بگو.
- نام مشتری، شرکت یا هر چیزی که هویت کسی را لو بدهد نیاور. مشاهده را بی‌نام کن.
- keyPoints باید «ادعا» باشند، نه تیتر.
- hookAngle باید درد یا تناقضی را بگوید که مخاطب در کار خودش تشخیص می‌دهد.
- بریف را پلتفرم‌خنثی بنویس؛ خودِ کپی‌رایتر لباس لینکدین را می‌پوشاند.${lessons}`;

  const source = input.observation
    ? input.observationIsTrusted
      ? `مشاهده‌ی اپراتور (تجربه‌ی واقعی و قابل استناد — منبع اصلی این پست):
${input.observation}`
      : `زاویه‌ی پیشنهادی (این متن را **خود سیستم** تولید کرده، نه یک انسان):
${input.observation}

⚠️ این متن «واقعیت» نیست، فقط یک جهت است. هر عدد، درصد، آمار یا جزئیات موردیِ مشخصی که در آن آمده **ساختگی است** و نباید در خروجی تو ظاهر شود. از آن فقط موضوع و زاویه را بردار؛ ادعاهای عددی را به بیان کیفی تبدیل کن (مثلاً به‌جای «۱۰٪ رشد فروش و ۲٪ کاهش سود»، بنویس «فروش بالا رفته ولی سود پایین آمده»).`
    : `مشاهده‌ای ثبت نشده است؛ از ایده‌های زیر استفاده کن و بهترین را انتخاب کن.
توجه: چون تجربه‌ی دست‌اول در کار نیست، بیشتر مراقب باش که محتوا به کلی‌گویی نیفتد.

${input.ideas
  .map((i, n) => `${n + 1}. «${i.title}» (امتیاز ${i.score})\n   قلاب: ${i.hook}\n   درد مخاطب: ${i.painPoint}`)
  .join("\n")}`;

  const prompt = `${source}

بر این اساس بریف اجتماعی بنویس.`;

  return runAgentJSON({
    agent: "linkedin-angle-finder",
    system,
    prompt,
    temperature: 0.5,
    schema: SocialBriefSchema,
    shapeHint: `{
  "coreMessage": "درس عمومی که از این مشاهده بیرون می‌آید",
  "audience": "مخاطب مشخص این پست",
  "keyPoints": ["ادعای مستقل اول", "ادعای دوم", "ادعای سوم"],
  "hookAngle": "تناقض یا دردی که مخاطب در کار خودش می‌بیند",
  "proofPoint": "جزئیات ملموس و بی‌نام از همان مشاهده",
  "cta": "پرسشی که گفت‌وگو راه می‌اندازد"
}`,
  });
}
