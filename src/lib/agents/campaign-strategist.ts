import "server-only";
import { runAgentJSON } from "@/lib/ai";
import { COMPANY_PROFILE, BRAND_VOICE } from "@/lib/company";
import { lessonsBlockFor } from "./lessons";
import { CampaignNarrativeSchema, type CampaignNarrative } from "./types";

/**
 * ایجنت — استراتژیست کمپین
 *
 * از یک تم، «روایت مادر» می‌سازد: ایده‌ی مرکزی، تنش، پاسخ، و زاویه‌ی
 * اختصاصی هر کانال.
 *
 * چرا این لایه لازم است؟ بدون آن، اجرای هم‌زمانِ چهار پایپ‌لاین با یک
 * موضوع مشترک، چهار محتوای بی‌ربط با یک برچسب مشترک می‌دهد. کمپین یعنی
 * یک حرف که در چند کانال با لباس‌های مختلف تکرار می‌شود — و کسی که هر سه
 * را ببیند، باید حس کند یک نفر با او حرف می‌زند، نه سه نفر.
 */
export async function runCampaignStrategist(input: {
  theme: string;
  /** عنوان محتواهای قبلی، برای پرهیز از تکرار */
  existingTitles: string[];
}): Promise<CampaignNarrative> {
  const lessons = await lessonsBlockFor("campaign-strategist");

  const system = `تو «استراتژیست کمپین» آرکان هستی. از یک تم، یک روایت مادر می‌سازی که بعداً در بلاگ، اینستاگرام و لینکدین با لباس‌های متفاوت تکرار می‌شود.

${COMPANY_PROFILE}

${BRAND_VOICE}

قواعد:
- **یک حرف، نه چند حرف.** bigIdea باید آن‌قدر مشخص باشد که بشود در یک جمله گفت و آن‌قدر بزرگ که سه محتوای متفاوت از آن دربیاید. اگر نتوانستی در یک جمله بگویی‌اش، هنوز به‌اندازه‌ی کافی تیز نشده.
- tension باید یک تناقض واقعی در کار مخاطب باشد، نه یک «مشکل» کلی. مثال خوب: «هرچه بیشتر استخدام می‌کنند، کندتر می‌شوند». مثال بد: «مدیریت منابع انسانی سخت است».
- زاویه‌ی هر کانال باید **متفاوت** باشد، نه خلاصه‌ی همان چیز:
  · بلاگ = عمق و چارچوب؛ جایی که کسی که دنبال جواب می‌گردد پیدایش می‌کند.
  · اینستاگرام = یک برش بصری و سریع؛ چیزی که اسکرول را متوقف کند.
  · لینکدین = مشاهده و تجربه؛ چیزی که گفت‌وگوی حرفه‌ای راه بیندازد.
  اگر هر سه زاویه شبیه هم درآمدند، کمپین شکست خورده — دوباره فکر کن.
- **هیچ عدد، درصد یا آماری در هیچ‌کدام از فیلدها نساز.** این مهم‌ترین قاعده است: زاویه‌هایی که می‌نویسی مستقیم به کپی‌رایترها داده می‌شوند و آن‌ها فرض می‌کنند هرچه در ورودی‌شان هست واقعی است. نوشتن «شرکتی با ۱۰٪ رشد فروش که ۲٪ سود از دست داد» یعنی ساختن یک مطالعه‌ی موردی جعلی که به اسم آرکان منتشر می‌شود. به‌جای عدد، رابطه را بگو: «فروش بالا می‌رود ولی سود پایین می‌آید».${lessons}`;

  const existing =
    input.existingTitles.length > 0
      ? `\n\nمحتواهای موجود (تم تکراری نده):\n${input.existingTitles.slice(0, 20).map((t) => `- ${t}`).join("\n")}`
      : "";

  const prompt = `تم کمپین: «${input.theme}»${existing}

روایت مادر این کمپین را بساز.`;

  return runAgentJSON({
    agent: "campaign-strategist",
    system,
    prompt,
    temperature: 0.6,
    schema: CampaignNarrativeSchema,
    shapeHint: `{
  "bigIdea": "یک جمله که کل کمپین حول آن می‌چرخد",
  "audience": "مخاطب مشخص کمپین",
  "tension": "تناقض واقعی در کار مخاطب",
  "resolution": "پاسخ آرکان به آن تنش",
  "pillars": ["ستون محتوایی اول", "دوم", "سوم"],
  "blogAngle": "زاویه‌ی مقاله — عمق و چارچوب",
  "instagramAngle": "زاویه‌ی کاروسل — برش سریع و بصری",
  "linkedinAngle": "زاویه‌ی لینکدین — مشاهده و تجربه"
}`,
  });
}
