import "server-only";
import { runAgentJSON } from "@/lib/ai";
import { COMPANY_PROFILE, BRAND_VOICE } from "@/lib/company";
import { lessonsBlockFor } from "./lessons";
import { SocialBriefSchema, type SocialBrief } from "./types";
import type { Post } from "@/lib/store";

/**
 * ایجنت ۱ (پایپ‌لاین بازآفرینی) — بازآفرین محتوا (Repurposer)
 *
 * وظیفه: از یک مقاله‌ی منتشرشده، یک «بریف اجتماعی» بسازد که هر دو
 * کپی‌رایتر (اینستاگرام و لینکدین) از روی آن بنویسند.
 *
 * نکته‌ی آموزشی — چرا یک بریف مشترک و نه دو نویسنده‌ی مستقل روی مقاله؟
 * چون اتمیزه‌کردن محتوا یعنی «یک پیام، چند لباس». اگر هر کپی‌رایتر خودش
 * مستقیم مقاله را می‌خواند، هرکدام ایده‌ی متفاوتی برمی‌داشت و دو محتوای
 * بی‌ربط تولید می‌شد. این همان نقشی است که استراتژیست در پایپ‌لاین بلاگ دارد.
 */
export async function runRepurposer(input: {
  post: Post;
}): Promise<SocialBrief> {
  const lessons = await lessonsBlockFor("repurposer");

  const system = `تو «بازآفرین محتوا»ی آرکان هستی. کارت این است که از یک مقاله‌ی بلند، آن یک ایده‌ای را بیرون بکشی که در فید شبکه‌های اجتماعی زنده می‌ماند.

${COMPANY_PROFILE}

${BRAND_VOICE}

قواعد سخت‌گیرانه:
- مقاله را **خلاصه نکن**. خلاصه‌ی یک مقاله‌ی هزار کلمه‌ای، محتوای کسل‌کننده‌ای برای فید می‌شود. یک ایده انتخاب کن که به‌تنهایی ارزش توقف اسکرول را دارد.
- keyPoints باید «ادعا» باشند، نه تیتر. هر نکته باید مستقل خوانده و فهمیده شود. «سه اشتباه قیمت‌گذاری» تیتر است؛ «بیشتر کسب‌وکارها قیمت را از روی هزینه می‌بندند، نه از روی ارزشی که می‌سازند» ادعاست.
- proofPoint فقط از دل همین مقاله دربیاید. عدد، آمار یا مثالی که در مقاله نیست، از خودت نساز.
- hookAngle باید «درد مخاطب» را بگوید، نه موضوع مقاله را. نه «درباره‌ی قیمت‌گذاری»، بلکه «چرا با وجود فروش خوب، سود نمی‌ماند».
- بریف باید پلتفرم‌خنثی بماند: از «سوایپ کن»، «کامنت بگذار» یا هر اصطلاح مخصوص یک شبکه استفاده نکن. لباس را کپی‌رایترها می‌پوشانند.${lessons}`;

  const prompt = `مقاله‌ی مبدأ:

عنوان: ${input.post.title}
چکیده: ${input.post.excerpt}

متن مقاله:
${input.post.contentMd.slice(0, 8000)}

یک بریف اجتماعی بساز.`;

  return runAgentJSON({
    agent: "repurposer",
    system,
    prompt,
    temperature: 0.4,
    schema: SocialBriefSchema,
    shapeHint: `{
  "coreMessage": "تنها ایده‌ای که ارزش انتقال به فید را دارد",
  "audience": "مخاطب مشخص این محتوا",
  "keyPoints": ["ادعای مستقل اول", "ادعای دوم", "ادعای سوم"],
  "hookAngle": "دردی که مخاطب را متوقف می‌کند",
  "proofPoint": "مثال یا عددی از دل مقاله",
  "cta": "دعوت طبیعی به قدم بعدی"
}`,
  });
}
